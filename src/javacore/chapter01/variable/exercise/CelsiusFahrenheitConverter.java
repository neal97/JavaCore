package javacore.chapter01.variable.exercise;

public class CelsiusFahrenheitConverter {
    public static void main(String[]  args) {

        double Celsius = 100 ;

        double Fahrenheit = (Celsius * 1.8) + 32 ;


        System.out.println(Celsius + "C° font " + Fahrenheit + "F°");

    }

}
